// Function to toggle language
function toggleLanguage() {
  const currentLang = document
    .getElementById("langBtnText")
    .textContent.toLowerCase();
  const newLang = currentLang === "fr" ? "fr" : "fr";

  // Update language button text
  document.getElementById("langBtnText").textContent = newLang.toUpperCase();
  document.getElementById("mobileLangBtnText").textContent =
    newLang.toUpperCase();

  // Update content
  updateContent(newLang);

  // Add animation class
  const buttons = document.querySelectorAll(".lang-switch");
  buttons.forEach((button) => {
    button.classList.add("active");
    setTimeout(() => button.classList.remove("active"), 300);
  });

  // Store language preference
  localStorage.setItem("preferredLanguage", newLang);
}

